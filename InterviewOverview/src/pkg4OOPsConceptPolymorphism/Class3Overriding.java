package pkg4OOPsConceptPolymorphism;

public class Class3Overriding {
	// Overriding -->method can be called as overridden method when name of method
	// and signature in the child and parent class is same
	//Inheritance is must for overriding method 
	
	
	
	public void method1() {
		System.out.println("Method1 parent class");
	}
	
	public static void method2() {
		System.out.println("Method2 static parent class");
	}
	
	
	
	
}
